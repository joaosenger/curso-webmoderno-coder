const a = 7 // coloca o valor 7 dentro da const a
let b = 3

b += a // atribuição aditiva b = b + a
console.log(b)

b -= 4 // atribuição subtrativa b = b - 4
console.log(b)

b *= 2 // atribuição multiplicativa b = b * 2
console.log(b)

b /= 2 // atribuição divisiva b = b / 2
console.log(b)

b %= 2 // resto da divisão b = b % 2 (nesse exemplo, ficaria: 6 / 2 é 3 e sobra 0, essa sobra que será exibida)
console.log(b)